def add(n1,n2):
  return n1+n2
def subtract(n1,n2):
  return n1-n2
def multiply(n1,n2):
  return n1*n2
def divide(n1,n2):
  return n1/n2
operations={
            '+':add,
            '-':subtract,
            '*':multiply,
            '/':divide
            }

def calculator():
  print(logo)
  should_continue=True
  first_num=float(input('What is the first number?'))
  while should_continue:
    operator=input('Pick an operation: +, -, * or /.')
    second_num=float(input('What is the second number?'))
    calc_function=operations[operator]
    result=calc_function(first_num,second_num)
    print(f'{first_num}{operator}{second_num}={result}')
    if input("Type 'yes' if you want to continue with the previous answer. Type 'no' to begin a new calculation.")=='yes':
      first_num=result
    else:
      should_continue=False
      replit.clear()
      calculator()
calculator()      
